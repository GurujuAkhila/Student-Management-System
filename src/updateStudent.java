    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNumber1 = jTextField1.getText();
        String name = jTextField2.getText();
        String rollNumber = jTextField3.getText();
        String phoneNumber = jTextField4.getText();
        String course = jTextField5.getText();
        String gender = jTextField6.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("update student set name='"+name+"',rollNumber='"+rollNumber+"',contactNo='"+phoneNumber+"',course='"+course+"',gender='"+gender+"' where rollNumber='"+rollNumber1+"'");
            JOptionPane.showMessageDialog(null,"Successfully Updated");
            setVisible(false);
            new updateStudent().setVisible(true);
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

